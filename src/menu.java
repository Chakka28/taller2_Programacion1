    private void matriz() {
        int n = 0;
        String cadena2 = lineas.getText();
        String cadena3 = columnas.getText();
        int numEntero = Integer.parseInt(cadena2);
        int numEntero1 = Integer.parseInt(cadena3);
        String[][] numeros = new String[numEntero][numEntero1];
        
        for (int x = 0; x < numeros.length; x++) {
            for (int y = 0; y < numeros[x].length; y++) {
                int rrr = (int) (Math.random()*9+1);
                String rr = String.valueOf(rrr);
               numeros[x][y] = rr;
            }
        }

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setNumRows(numEntero);
        model.setRowCount(numEntero1);
        for (int x = 0; x < numEntero; x++) {
            for (int j = 0; j < numEntero1; j++) {
                jTable1.setValueAt(numeros[x][j], x, j);
            }
        }
    }

