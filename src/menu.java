    private void intentos() {

        cont++;
        String cadena2 = jTextField1.getText();
        int numEntero = Integer.parseInt(cadena2);
        if (numEntero == numrandom) {
            jTextField4.setText("HAS GANADO");
            jTextField3.setVisible(false);
            jTextField2.setVisible(false);
            for (int i = 0; i < jPanel1.getComponents().length; i++) {
                jPanel1.getComponent(i).setEnabled(false);
            }

            for (Component c : jPanel1.getComponents()) {
                c.setEnabled(false);
            }

        }
        if (cont == 3) {
            jTextField2.setText("has perdido");
            for (int i = 0; i < jPanel1.getComponents().length; i++) {
                jPanel1.getComponent(i).setEnabled(false);
            }

            for (Component c : jPanel1.getComponents()) {
                c.setEnabled(false);
            }
        }
        if (cont < 3) {
            String adena1 = String.valueOf(cont);
            jTextField2.setText(adena1);
            jTextField3.setText("NO ES IGUAL");

        }

    }

